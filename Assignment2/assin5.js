//named function
function arrayTotal(array)
{
   var sum=0;
   for(var i=0;i<array.length;i++)
   {
    sum = sum+array[i]
   }
console.log("Sum of array is: "+sum)
}

var array =[10,20,30]
arrayTotal(array)


